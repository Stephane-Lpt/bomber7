package com.bomber7.utils;

/**
 * Enum representing different types of sounds used in the game.
 */
public enum SoundType {
    /**
     * Sound played when hovering over a UI element.
     */
    HOVER,

    /**
     * Sound played when clicking on a UI element.
     */
    CLICK,

    /**
     * Sound played when a bomb explodes.
     */
    EXPLOSION,

    /**
     * Beast mode music.
     */
    BEAST_MODE,

    /**
     * Elevator music.
     */
    ELEVATOR,

    /**
     * Brain rot music.
     */
    BRAIN_ROT,

    /**
     * Epic fight music.
     */
    EPIC_FIGHT_1,
    /**
     * Epic fight music.
     */
    EPIC_FIGHT_2,
    /**
     * Epic fight music.
     */
    EPIC_FIGHT_3,

    /**
     * Guitar music.
     */
    GUITAR,

    /**
     * Charging bomb sound effect.
     */
    BOMB_CHARGE,

    /**
     * Footstep sound effect.
     */
    FOOTSTEP_1,
    /**
     * Footstep sound effect.
     */
    FOOTSTEP_2,
    /**
     * Footstep sound effect.
     */
    FOOTSTEP_3,

    /**
     * Game over sound.
     */
    GAME_OVER,
}

